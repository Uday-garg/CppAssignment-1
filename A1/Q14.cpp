#include <iostream>
int main() {
    // declare double variable force
    double force; 
    // store 172.5 in variable force
    force = 172.5;
    // declare double variable area
    double area;
    // store 27.5 in variable area
    area = 27.5;
    // declare double variable pressure
    double pressure;
    // divide force by area to calculate and store in the variable pressure
    pressure = force / area;
    // display calculated pressure
    std::cout << pressure;
}
