#include <iostream>
int main() {
    // declare integer variable speed
    int speed; 
    // store 20 in variable speed
    speed = 20;
    // declare integer variable time
    int time;
    // store 10 in variable time
    time = 10;
    // declare integer variable distance
    int distance;
    // multiple speed and time to calculate and store in the variable distance
    distance = speed * time;
    // display calculated distance
    std::cout << distance;
}
